CREATE DATABASE STORE

USE STORE

CREATE TABLE PRODUCT(ID_PRODUCT VARCHAR(50) PRIMARY KEY NOT NULL, NAME VARCHAR(50));

CREATE TABLE CUSTOMER(ID_CUSTOMER INT PRIMARY KEY NOT NULL, NAME VARCHAR(50), LASTNAME VARCHAR(50))

CREATE TABLE SALES(ID_SALES INT PRIMARY KEY NOT NULL, ID_PRODUCT VARCHAR(50) FOREIGN KEY REFERENCES PRODUCT(ID_PRODUCT) NOT NULL,
ID_CUSTOMER INT FOREIGN KEY REFERENCES CUSTOMER(ID_CUSTOMER) NOT NULL, PAYMENT_DATE DATETIME, AMOUNT FLOAT)
